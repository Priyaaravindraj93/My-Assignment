package week1.day2;

public class SwitchCase2 {

	public static void main(String[] args) {

int num=2;
switch(num)
{
case 0: 
	System.out.println("The num is zero");
	break;
	
case 1: 
	System.out.println("The num is greater than zero");
	break;

case 2: 
	System.out.println("The num is lesser than zero");
	break;
	
}

	}

}
