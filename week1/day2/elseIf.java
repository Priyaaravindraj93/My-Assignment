package week1.day2;

public class LearnElseIf {

	public static void main(String[] args) {
		int a=5;
		int b=5;
		if(a<b) {
			System.out.println("A is less than B");
		}
		else if(a>b) {
			System.out.println("A is greater than B");
		}
		else {
			System.out.println("Both are Equal");
		}

	}
}
